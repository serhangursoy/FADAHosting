function bestTwoFinder(num) {
  let sqrNum = Math.sqrt(num);
  let res = [];
  if (Number.isInteger(sqrNum)) {
    return [sqrNum, sqrNum];
  } else {
    let someTmp = parseInt(sqrNum);
    let isOkay = false;
    while (!isOkay) {
      if (num % someTmp == 0) {
        res[0] = someTmp;
        isOkay = true;
      } else {
        someTmp--;
      }
    }

    someTmp = parseInt(sqrNum) + 1;
    isOkay = false;
    while (!isOkay) {
      if (num % someTmp == 0) {
        res[1] = someTmp;
        isOkay = true;
      } else {
        someTmp++;
      }
    }

    if (res[0] < res[1]) {
      let tmp = res[0];
      res[0] = res[1];
      res[1] = tmp;
    }
    return res;
  }
}

